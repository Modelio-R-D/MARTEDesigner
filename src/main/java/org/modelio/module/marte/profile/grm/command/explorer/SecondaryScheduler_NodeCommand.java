@objid ("5d7dd38a-c13d-4d64-a259-8c344ee0909f")
public class SecondaryScheduler_NodeCommand extends DefaultModuleCommandHandler {
    @objid ("0395b0ed-251a-4fb6-ae34-0f5cbc0a4694")
    @Override
    public boolean accept(List<MObject> selected_element, IModule module) {
        if(selected_element.size() > 0 && selected_element.get(0) instanceof ModelElement){
            ModelElement element = (ModelElement)selected_element.get(0);
            // Audit constraint adding
            if (element instanceof Class || element instanceof Collaboration || element instanceof Actor || element instanceof UseCase || element instanceof Signal || element instanceof Interface || element instanceof Enumeration){
                return false;
            }
         if(element instanceof ModelTree){       return true;    }  
         if(element instanceof TemplateParameter){       return true;    }  
        }
        return false;
    }

    @objid ("5e3fa877-afbc-4146-af9a-aa937e10bf58")
    @Override
    public void actionPerformed(List<MObject> selected_element, IModule module) {
        try(ITransaction tr = MARTEModule.getInstance().getModuleContext().getModelingSession().createTransaction("SecondaryScheduler_NodeCommand")){
        
        
            ModelElement parent = (ModelElement)selected_element.get(0);        
            SecondaryScheduler_Node proxy = new SecondaryScheduler_Node();
        
        if(parent instanceof ModelTree){
            proxy.setParent((ModelTree) parent);
        }
        if(parent instanceof TemplateParameter){
            proxy.setParent((TemplateParameter) parent);
        }      
        MARTEModule.getInstance().getModuleContext().getModelingSession().getModel().getDefaultNameService().setDefaultName(proxy.getElement(),proxy.getElement().getName());
                 
            
            
           tr.commit();
         
              
         
            
        
           
                
        }
    }

}
